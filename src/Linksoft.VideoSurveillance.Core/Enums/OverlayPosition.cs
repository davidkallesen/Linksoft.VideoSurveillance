/// <summary>
/// Specifies the position of the camera overlay.
/// </summary>
public enum OverlayPosition
{
    /// <summary>
    /// Overlay positioned at the top-left corner (default).
    /// </summary>
    TopLeft,

    /// <summary>
    /// Overlay positioned at the top-right corner.
    /// </summary>
    TopRight,

    /// <summary>
    /// Overlay positioned at the bottom-left corner.
    /// </summary>
    BottomLeft,

    /// <summary>
    /// Overlay positioned at the bottom-right corner.
    /// </summary>
    BottomRight,
}